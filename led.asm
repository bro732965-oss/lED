; =========================================
; executor.asm - ИСПОЛНИТЕЛЬ
; Спрашивает имя файла, загружает и выполняет
; =========================================

.include "m328pdef.inc"
.include "name.asm"          ; R0C0, R0C1, ..., R9C9

; ===== ОПКОДЫ (твои команды) =====
.equ OP_LED_ON   = 0x01
.equ OP_LED_OFF  = 0x02
.equ OP_RDG_KEY  = 0x03
.equ OP_CND      = 0x04
.equ OP_DELAY    = 0x05
.equ OP_END      = 0xFF

; ===== БУФЕРЫ =====
.dseg
.org 0x0100
filename:   .byte 20        ; Имя файла
program:    .byte 256       ; Загруженная программа
last_key:   .byte 1         ; Последняя нажатая клавиша

.cseg
.org 0x0000

main:
    call init_uart          ; Для общения с терминалом
    call init_matrix        ; Настроить светодиоды

    ; =====================================
    ; 1. СПРОСИТЬ ИМЯ ФАЙЛА
    ; =====================================
ask_filename:
    call print_string
    .db "Введите имя файла: ", 0

    call read_string        ; Читаем имя файла в filename

    ; =====================================
    ; 2. ЗАГРУЗИТЬ ФАЙЛ
    ; =====================================
    call load_file

    ; =====================================
    ; 3. ВЫПОЛНИТЬ
    ; =====================================
    call execute_program

    ; =====================================
    ; 4. ГОТОВО
    ; =====================================
    call print_string
    .db "Готово!", 0

loop:
    rjmp loop

; =========================================
; ЗАГРУЗКА ФАЙЛА
; =========================================
load_file:
    ; Читаем файл и переводим команды в байты
    ; Твой язык → байты:
    ; led-on R9C9 → 0x01 0x63
    ; led-off R9C9 → 0x02 0x63
    ; rdg.key     → 0x03
    ; cnd A       → 0x04 0x41 (если A)
    ; delay 100   → 0x05 0x64
    ret

; =========================================
; ВЫПОЛНЕНИЕ ПРОГРАММЫ
; =========================================
execute_program:
    ldi r30, low(program)
    ldi r31, high(program)

execute_loop:
    ld r16, X+               ; Читаем команду

    cpi r16, OP_LED_ON
    breq exec_led_on

    cpi r16, OP_LED_OFF
    breq exec_led_off

    cpi r16, OP_RDG_KEY
    breq exec_rdg_key

    cpi r16, OP_CND
    breq exec_cnd

    cpi r16, OP_DELAY
    breq exec_delay

    cpi r16, OP_END
    breq exec_end

    rjmp execute_loop

; =========================================
; led-on # — ВКЛЮЧИТЬ СВЕТОДИОД
; =========================================
exec_led_on:
    ld r16, X+               ; Читаем номер пина (0-99)
    call set_led_on
    rjmp execute_loop

; =========================================
; led-off # — ВЫКЛЮЧИТЬ СВЕТОДИОД
; =========================================
exec_led_off:
    ld r16, X+
    call set_led_off
    rjmp execute_loop

; =========================================
; rdg.key — ПРОЧИТАТЬ КЛАВИШУ
; =========================================
exec_rdg_key:
    call read_key_from_uart
    sts last_key, r16
    rjmp execute_loop

; =========================================
; cnd — УСЛОВИЕ
; =========================================
exec_cnd:
    ld r16, X+               ; Читаем значение для сравнения (ASCII)
    lds r17, last_key        ; Загружаем последнюю клавишу
    cp r16, r17
    breq exec_cnd_true
    ; Если не равно — пропускаем следующую команду и её аргумент
    ld r16, X+               ; Пропускаем команду
    ld r16, X+               ; Пропускаем аргумент
    rjmp execute_loop
exec_cnd_true:
    ; Если равно — выполняем следующую команду
    rjmp execute_loop

; =========================================
; delay — ЗАДЕРЖКА
; =========================================
exec_delay:
    ld r24, X+               ; Младший байт
    ld r25, X+               ; Старший байт
    call delay_ms
    rjmp execute_loop

; =========================================
; КОНЕЦ ПРОГРАММЫ
; =========================================
exec_end:
    ret

; =========================================
; set_led_on — ВКЛЮЧИТЬ ПО НОМЕРУ
; =========================================
set_led_on:
    ; r16 = номер светодиода (0-99)
    ; Превращаем в строку и столбец
    ; Зажигаем в матрице 10×10
    ret

; =========================================
; set_led_off — ВЫКЛЮЧИТЬ ПО НОМЕРУ
; =========================================
set_led_off:
    ; r16 = номер светодиода (0-99)
    ret

; =========================================
; init_matrix — НАСТРОЙКА ВСЕХ ПИНОВ
; =========================================
init_matrix:
    ldi r16, 0xFF
    out DDRB, r16
    out DDRC, r16
    out DDRD, r16
    ret

; =========================================
; init_uart — НАСТРОЙКА UART
; =========================================
init_uart:
    ; Настроить UART (скорость 9600, 8 бит, 1 стоп)
    ret

; =========================================
; print_string — ВЫВОД СТРОКИ
; =========================================
print_string:
    ; Текст после вызова
    ret

; =========================================
; read_string — ЧТЕНИЕ СТРОКИ
; =========================================
read_string:
    ; Читаем с UART до Enter
    ret

; =========================================
; read_key_from_uart — ЧТЕНИЕ КЛАВИШИ
; =========================================
read_key_from_uart:
    ; Ждём символ с UART
    ; Возвращаем в r16
    ret

; =========================================
; delay_ms — ЗАДЕРЖКА В МИЛЛИСЕКУНДАХ
; =========================================
delay_ms:
    ; r24:r25 = количество миллисекунд
    ret