; =========================================
; sd.asm - ПРОСТОЙ ДРАЙВЕР SD (без FAT)
; =========================================

.include "m328pdef.inc"

.equ SD_CS   = PB2
.equ SD_SCK  = PB5
.equ SD_MOSI = PB3
.equ SD_MISO = PB4

; =========================================
; sd_init - ИНИЦИАЛИЗАЦИЯ
; =========================================
sd_init:
    sbi DDRB, SD_CS
    sbi DDRB, SD_SCK
    sbi DDRB, SD_MOSI
    cbi DDRB, SD_MISO
    sbi PORTB, SD_CS
    call spi_init
    ret

; =========================================
; sd_read_byte - ЧИТАТЬ БАЙТ
; =========================================
sd_read_byte:
    ; Вход: r24 = адрес (младший), r25 = адрес (старший)
    ; Выход: r16 = байт
    
    call sd_read_block
    ret

; =========================================
; sd_write_byte - ЗАПИСАТЬ БАЙТ
; =========================================
sd_write_byte:
    ; Вход: r24 = адрес (младший), r25 = адрес (старший)
    ; Вход: r16 = байт для записи
    
    call sd_write_block
    ret

; =========================================
; sd_read_block - ЧИТАТЬ 512 БАЙТ
; =========================================
sd_read_block:
    ; Читает блок (сектор) 512 байт
    ret

; =========================================
; sd_write_block - ЗАПИСАТЬ 512 БАЙТ
; =========================================
sd_write_block:
    ; Записывает блок (сектор) 512 байт
    ret