; =========================================
; audio.asm - АУДИОПОРТ
; =========================================

.include "m328pdef.inc"

; ===== ОПКОДЫ =====
.equ F0xA1 = 0xA1   ; audio-on
.equ F0xA2 = 0xA2   ; audio-off

; ===== ПИН ДЛЯ ДИНАМИКА =====
.equ SPEAKER_PIN = PB3

; ===== КОНСТАНТЫ ДЛЯ ЧАСТОТ =====
; Для F_CPU = 16 МГц, предделитель 64
; OCR2A = (125000 / частота) - 1
.equ NOTE_C4 = 229   ; 262 Гц
.equ NOTE_D4 = 204   ; 294 Гц
.equ NOTE_E4 = 182   ; 330 Гц
.equ NOTE_F4 = 172   ; 349 Гц
.equ NOTE_G4 = 153   ; 392 Гц
.equ NOTE_A4 = 136   ; 440 Гц
.equ NOTE_B4 = 121   ; 494 Гц
.equ NOTE_C5 = 114   ; 523 Гц

; =========================================
; audio-on [частота] — ВКЛЮЧИТЬ ЗВУК
; =========================================
exec_audio_on:
    ; r17:r18 = частота в Гц
    ; Пока используем готовые константы
    
    ; Настраиваем таймер 2 в режим CTC
    ldi r16, (1<<WGM21) | (1<<COM2A0)
    out TCCR2A, r16
    
    ; Устанавливаем частоту (пока заглушка)
    ldi r16, NOTE_A4   ; 440 Гц
    out OCR2A, r16
    
    ; Запускаем таймер с предделителем 64
    ldi r16, (1<<CS22)
    out TCCR2B, r16
    
    ; Настраиваем ножку на выход
    sbi DDRB, SPEAKER_PIN
    
    rjmp execute_loop

; =========================================
; audio-off — ВЫКЛЮЧИТЬ ЗВУК
; =========================================
exec_audio_off:
    ; Останавливаем таймер
    clr r16
    out TCCR2B, r16
    out TCCR2A, r16
    
    ; Выключаем ножку
    cbi PORTB, SPEAKER_PIN
    cbi DDRB, SPEAKER_PIN   ; Опционально: перевести в вход
    
    rjmp execute_loop