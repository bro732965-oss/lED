# ATMEGA328P EXECUTOR - СБОРКА

## КОМПЛЕКТУЮЩИЕ
- ATmega328P (Arduino Nano) - 1 шт
- Светодиоды 5мм - 100 шт
- Резисторы 220 Ом - 100 шт
- Резисторы 10 кОм - 10 шт
- Резистор 100 Ом - 1 шт
- Динамик 8 Ом - 1 шт
- Кнопки тактовые - 10 шт
- Модуль SD-карты - 1 шт (опционально)
- Макетная плата - 1 шт
- Провода DuPont - много
- USB-кабель - 1 шт
- Блок питания 5В/2А - 1 шт

## ПИНЫ МИКРОКОНТРОЛЛЕРА
PB0=D8  PB1=D9  PB2=D10 PB3=D11 PB4=D12 PB5=D13
PC0=A0  PC1=A1  PC2=A2  PC3=A3  PC4=A4  PC5=A5
PD0=D0  PD1=D1  PD2=D2  PD3=D3  PD4=D4  PD5=D5  PD6=D6  PD7=D7

## ПОДКЛЮЧЕНИЕ МАТРИЦЫ 10×10
Строки (катоды, активный 0):
R0→PD0 R1→PD1 R2→PD2 R3→PD3 R4→PD4 R5→PD5 R6→PD6 R7→PD7 R8→PC2 R9→PC3

Столбцы (аноды, активный 1):
C0→PB0 C1→PB1 C2→PB2 C3→PB3 C4→PB4 C5→PB5 C6→PC0 C7→PC1 C8→PC2 C9→PC3

ВНИМАНИЕ: PC2 и PC3 используются дважды! Исправьте:
Строки: PD0-PD7 + PC4-PC5
Столбцы: PB0-PB5 + PC0-PC1

## ПОДКЛЮЧЕНИЕ КЛАВИАТУРЫ (0-9)
0→PC0  1→PC1  2→PD0  3→PD1  4→PD2  5→PD3  6→PD4  7→PD5  8→PD6  9→PD7

## ПОДКЛЮЧЕНИЕ ДИНАМИКА
PB3 → резистор 100 Ом → динамик → GND

## ПОДКЛЮЧЕНИЕ SD-КАРТЫ
PB2→CS  PB3→MOSI  PB4→MISO  PB5→SCK  VCC→5V  GND→GND

## ПОДКЛЮЧЕНИЕ UART (к компьютеру)
PB0(RX)→USB-TX  PB1(TX)→USB-RX  GND→GND

## КАК ПРОВЕРИТЬ ПОДКЛЮЧЕНИЕ
1. Залейте тестовую прошивку
2. Подключите USB
3. Откройте монитор порта (9600 бод)
4. Отправьте: 01 00 FF
5. Должен загореться LED #0

## ПИТАНИЕ
- Arduino через USB: 5В
- Если много светодиодов → внешний БП 5В/2А
- Подключите GND всех устройств вместе

## СБОРКА ПОШАГОВО
1. Вставьте Arduino в макетную плату
2. Подключите строки матрицы к пинам
3. Подключите столбцы матрицы к пинам
4. Подключите резисторы 220 Ом к анодам
5. Подключите кнопки с резисторами 10 кОм
6. Подключите динамик через резистор 100 Ом
7. Подключите SD-карту (опционально)
8. Проверьте все соединения
9. Подключите USB к компьютеру
10. Залейте прошивку

## СХЕМА КНОПКИ (подтяжка к GND)
          VCC
           |
           R (10 кОм)
           |
    Пин МК o----o Кнопка
           |
          GND

## РАСЧЁТ РЕЗИСТОРОВ
Светодиод: R = (5В - 2В) / 20мА = 150-220 Ом
Подтяжка: R = 10 кОм
Динамик: R = 100 Ом

## ТИПИЧНЫЕ ОШИБКИ
❌ Перепутаны анод и катод → светодиод не горит
❌ Нет резистора 220 Ом → сгорел светодиод
❌ Нет подтяжки 10 кОм → кнопка "шумит"
❌ PB3 занят динамиком и столбцом → конфликт
❌ Нет общего GND → не работает

## ПОЛЕЗНЫЕ СОВЕТЫ
- Используйте разные цвета светодиодов для наглядности
- Маркируйте провода (строки/столбцы)
- Начинайте с малой матрицы (3×3) для теста
- Используйте макетную плату с нумерацией
- Проверяйте мультиметром напряжение на пинах

